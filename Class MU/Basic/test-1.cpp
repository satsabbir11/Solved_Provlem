#include <stdio.h>

int main(){
	
    //print Hello World
	printf("Hello World\n");

	int a,b;
    

    //Add 1 and 2
	a=1, b=2;

	printf("%d\n",a+b);


	//Subtract 2 from 3
	a=3, b=2;

	printf("%d\n",a-b);

	//Multiply 4 with 5
	a=4, b=5;

	printf("%d\n",a*b);


	//Divide 6 by 2
	a=6, b=2;

	printf("%d\n",a/b);


	//MOD 20 and 3
	a=20, b=3;

	printf("%d\n",a%b);


	//pre increment
	printf("%d\n",++a);

	//post increment
	printf("%d\n",a++);

	//last value of a
	printf("%d\n",a);
}